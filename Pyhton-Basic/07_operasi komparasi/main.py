# Operasi Komparasi
# Setiap hasil dari oeprasi komparasi adalah boolean

# >, <, >=, <=, ==, !=, is, is not


a = 4
b = 5

# 1. Lebih besar  >
hasil = a > b
print(a ,">", b, "hasil :", hasil)


# 2. Lebih kecil <
hasil = a < b
print(a ,"<", b, "hasil :", hasil)


# 3. Lebih besar dari  >=
hasil = a >= b
print(a ,">=", b, "hasil :", hasil)


# 4. Lebih kecil dari  <=
hasil = a <= b
print(a ,"<=", b, "hasil :", hasil)


# 5. sama dengan  ==
hasil = a == b
print(a ,"==", b, "hasil :", hasil)


# 6. tidak sama dengan  !=
hasil = a != b
print(a ,"!=", b, "hasil :", hasil)


# 7. is
hasil = a is b
print(a ,"is", b, "hasil :", hasil)


# 8. is not
hasil = a is not b
print(a ,"is not", b, "hasil :", hasil)