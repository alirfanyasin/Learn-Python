# STRING

'''
  1. Menggunakan single quote '...'
  2. Menggunakan double quote "..."
'''

# Contoh
print('Menggunakan single quote')
print("Menggunakan double quote")

print('=============================')

# Contoh 2
print('"Halo, apa kabar?"')
print("'Halo, apa kabar?'")
print("Ini adalah hari jum'at")

print('=============================')

# Contoh 3
# Menggunakan tanda \ (membuat tanda \ menjadi string)
print('Mari shalat jum\'at')
print('g\' day, isn\'t it?')

print('=============================')


# Contoh 4
# Menggunakan backlash
print("C:\\user\\Irfan") 
print('=============================')


# Contoh 5
# Menggunakan tab (\t)
print("Hello, my name is \t Irfan")


# Contoh 6
# Menggunakan backspace (\b)
print("Irfan \bYasin")


# Conoth 7
# Menggunakan newline (\n)
print("Ini adalah baris pertama \nIni adalah baris kedua") # LF -> line feed 
print("Ini adalah baris pertama \rIni adalah baris kedua") # CR -> Carriage
print('=============================')


# Contoh 8
# Menggunakan raw string (r)
print(r'C:\User\Irfan')
print('=============================')



# Contoh 9
# Multiline literals string
print("""
  Nama : Irfan Yasin
  Kelas : 3
""")


# Contoh 10
# Multiline literal string dan raw
print(r""""
  Nama : Irfan Yasin
  Kelas : 1\10
  Website : https://irfanyasin.epizy.com
""")


